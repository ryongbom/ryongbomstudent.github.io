#include "stdafx.h"
#include <stdio.h>

#define MAX 1000

int main() {
	int dem, num, i, j;
	int nums[MAX], rems[MAX], start = -1;

	scanf_s("%d", &dem);
	scanf_s("%d", &num);

	// ?????? 
	if (dem % num == 0) {
		printf("%d", dem / num);
		return 0;
	}
	else
		printf("%d.", dem / num);

	dem = (dem % num) * 10;
	for (i = 0; i < MAX; i++) {
		// ???? ???? ???? ??
		for (j = 0; j < i; j++) {
			if (rems[j] == dem) {
				// ?? ???? ????? ???? ? ??? ???????? 
				start = j;
				break;
			}
		}
		// ??? ?????, ?? ???? ????? ???? 
		if (dem == 0 || start >= 0) break;

		nums[i] = dem / num;
		rems[i] = dem;

		dem = (dem % num) * 10;
	}

	// ????? 
	for (j = 0; j < i; j++) {
		// ???????? ???? 
		if (j == start)
			printf("(");
		printf("%d", nums[j]);
	}
	// ???? ????? ????? ??? ?? 
	if (start >= 0)
		printf(")");

	return 0;
}
